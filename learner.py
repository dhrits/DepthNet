class ModelSavingCallback(Callback):
    def __init__(self, model_name='model'):
        self.model_name = model_name
        self.best_loss = None

    def save_model(self, name):
        data = {}
        if hasattr(self.run.model, 'interpolation'):
            data['interpolation'] = self.run.model.interpolation
        if hasattr(self.run.model, 'output_size'):
            data['output_size'] = self.run.model.output_size
        if hasattr(self.run.model, 'decoder_only'):
            data['decoder_only'] = self.run.model.decoder_only
        data['name'] = type(self.run.model).__name__
        data['state_dict'] = self.run.model.state_dict()
        torch.save(data, name)

    def after_epoch(self):
        if self.best_loss is None or self.run.loss.cpu().item() < self.best_loss:
            self.run.logger("Best loss: ", self.best_loss, " Current loss: ", self.run.loss.cpu().item())
            self.run.logger("Loss improved. Saving model ...")
            self.best_loss = self.run.loss.cpu().item()
            self.save_model('{}_{}.pth'.format(self.model_name, self.epoch))

    def after_fit(self):
        self.run.logger("Saving final model...")
        self.save_model('{}_final.pth'.format(self.model_name))

    @classmethod
    def create_model_saving_callback(cls, name):
        def inner():
            return cls(name)
        return inner


