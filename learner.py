# Metrics
def accuracy(out, yb): return (torch.argmax(out, dim=1)==yb).float().mean()


def delta1(out, yb):
    maxRatio = torch.max(out/yb, yb /out)
    delta1 = float((maxRatio < 1.25).float().mean())
    return delta1


def delta2(out, yb):
    maxRatio = torch.max(out/yb, yb /out)
    delta1 = float((maxRatio < 1.25**2).float().mean())
    return delta1


def delta3(out, yb):
    maxRatio = torch.max(out/yb, yb /out)
    delta1 = float((maxRatio < 1.25**3).float().mean())
    return delta1


class AvgStats():
    def __init__(self, metrics, in_train): self.metrics, self.in_train = listify(metrics),in_train

    def reset(self):
        self.tot_loss,self.count = 0.,0
        self.tot_mets = [0.] * len(self.metrics)

    @property
    def all_stats(self): return [self.tot_loss.item()] + self.tot_mets

    @property
    def avg_stats(self):
        return [o/self.count for o in self.all_stats]

    def __repr__(self):
        if not self.count: return ""
        metric_names = ['loss'] + [m.__name__ for m in self.metrics]
        metrics = []
        for k, v in zip(metric_names, self.avg_stats):
            metrics.append(k + ': ' + str(v.item() if hasattr(v, 'item') else v))
        return f"{'train' if self.in_train else 'valid'}: " +  ', '.join(list(metrics))

    def accumulate(self, run):
        bn = run.xb.shape[0]
        self.tot_loss += run.loss * bn
        self.count += bn
        for i,m in enumerate(self.metrics):
            self.tot_mets[i] += m(run.pred, run.yb) * bn


class AvgStatsCallback(Callback):
    def __init__(self, metrics, tb=None):
        self.train_stats, self.valid_stats = AvgStats(metrics,True),AvgStats(metrics,False)
        self.tb = tb

    def begin_epoch(self):
        self.train_stats.reset()
        self.valid_stats.reset()

    def after_loss(self):
        stats = self.train_stats if self.in_train else self.valid_stats
        with torch.no_grad(): stats.accumulate(self.run)

    def after_epoch(self):
        self.run.logger(self.train_stats)
        self.run.logger(self.valid_stats)
        if self.tb:
            metric_names = ['loss'] + [m.__name__ for m in self.train_stats.metrics]
            for k, v in zip(metric_names, self.train_stats.avg_stats):
                self.tb.add_scalar(k, v, self.run.epoch)


class Recorder(Callback):
    def begin_fit(self):
        self.lrs = [[] for _ in self.opt.param_groups]
        self.losses = []

    def after_batch(self):
        if not self.in_train: return
        for pg,lr in zip(self.opt.param_groups,self.lrs): lr.append(pg['lr'])
        self.losses.append(self.loss.detach().cpu())

    def plot_lr  (self, pgid=-1): plt.plot(self.lrs[pgid])
    def plot_loss(self, skip_last=0): plt.plot(self.losses[:len(self.losses)-skip_last])


class ParamScheduler(Callback):
    _order=1
    def __init__(self, pname, sched_funcs): self.pname,self.sched_funcs = pname,sched_funcs

    def begin_fit(self):
        if not isinstance(self.sched_funcs, (list,tuple)):
            self.sched_funcs = [self.sched_funcs] * len(self.opt.param_groups)

    def set_param(self):
        assert len(self.opt.param_groups)==len(self.sched_funcs)
        for pg,f in zip(self.opt.param_groups,self.sched_funcs):
            pg[self.pname] = f(self.n_epochs/self.epochs)

    def begin_batch(self):
        if self.in_train: self.set_param()


class LR_Find(Callback):
    _order=1

    def __init__(self, max_iter=100, min_lr=1e-6, max_lr=10):
        self.max_iter,self.min_lr,self.max_lr = max_iter,min_lr,max_lr
        self.best_loss = 1e9

    def begin_batch(self):
        if not self.in_train: return
        pos = self.n_iter/self.max_iter
        lr = self.min_lr * (self.max_lr/self.min_lr) ** pos
        for pg in self.opt.param_groups: pg['lr'] = lr

    def after_step(self):
        if self.n_iter>=self.max_iter or self.loss>self.best_loss*10:
            raise CancelTrainException()
        if self.loss < self.best_loss: self.best_loss = self.loss

